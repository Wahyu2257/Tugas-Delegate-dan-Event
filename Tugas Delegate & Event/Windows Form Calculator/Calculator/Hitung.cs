using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator
{
    class Hitung
    {
        public int Jumlahkan(int a, int b)
        {
            return a + b;
        }

        public int Kurangkan(int a, int b)
        {
            return a - b;
        }
        public int Kalikan(int a, int b)
        {
            return a * b;
        }
        public int Bagikan(int a, int b)
        {
            return a / b;
        }
    }
}
